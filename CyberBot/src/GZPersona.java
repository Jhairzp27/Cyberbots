public abstract class GZPersona implements IGZHabilidades{
    private String gzHabilidades;

    public GZPersona() {
    }

    public String getGzHabilidades() {
        return gzHabilidades;
    }

    public void setGzHabilidades(String gzHabilidades) {
        this.gzHabilidades = gzHabilidades;
    }
}